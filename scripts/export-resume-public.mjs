#!/usr/bin/env node
import { readFileSync, writeFileSync } from "node:fs"
import { resumePrivateJsonPath, resumePublicTsPath } from "./resume-crypto.mjs"

function resolveRedactable(value) {
  return value.public
}

function toPublicResume(data) {
  return {
    meta: data.meta,
    links: data.links,
    basicInfo: {
      name: resolveRedactable(data.basicInfo.name),
      address: resolveRedactable(data.basicInfo.address),
      phone: resolveRedactable(data.basicInfo.phone),
      email: resolveRedactable(data.basicInfo.email),
      employmentType: data.basicInfo.employmentType,
    },
    summary: data.summary,
    skills: data.skills,
    experience: data.experience.map((section) => ({
      id: section.id,
      heading: resolveRedactable(section.heading),
      period: section.period,
      department: section.department,
      position: section.position,
      introBullets: section.introBullets,
      awards: section.awards,
      managementBullets: section.managementBullets,
      seBullets: section.seBullets,
      projects: section.projects.map((project) => ({
        id: project.id,
        title: project.title,
        period: project.period,
        role: project.role,
        teamSize: project.teamSize,
        techStack: project.techStack,
        achievements: project.achievements.map(resolveRedactable),
      })),
    })),
  }
}

const data = JSON.parse(readFileSync(resumePrivateJsonPath, "utf8"))
const publicResume = toPublicResume(data)
const serialized = JSON.stringify(publicResume, null, 2)

const output = `// Generated by pnpm export:resume — do not edit manually.
import type { PublicResumeData } from "./types"

export const publicResumeData = ${serialized} satisfies PublicResumeData
`

writeFileSync(resumePublicTsPath, output, "utf8")
console.log(`Exported ${resumePublicTsPath}`)
